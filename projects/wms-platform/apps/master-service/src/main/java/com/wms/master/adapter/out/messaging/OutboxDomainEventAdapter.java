package com.wms.master.adapter.out.messaging;

import com.wms.master.adapter.out.persistence.outbox.MasterOutboxEntity;
import com.wms.master.adapter.out.persistence.outbox.MasterOutboxRepository;
import com.wms.master.application.port.out.DomainEventPort;
import com.wms.master.domain.event.DomainEvent;
import java.util.List;

/**
 * {@link DomainEventPort} implementation that serializes each event into the
 * contract envelope and writes one {@code master_outbox} row per event. Invoked
 * inside the domain transaction so the state change and outbox row commit
 * atomically.
 *
 * <p>TASK-BE-438 (outbox v2): writes the v2 {@link MasterOutboxEntity} directly
 * via {@link MasterOutboxRepository}, replacing the v1 lib {@code OutboxWriter}
 * ({@code OutboxJpaEntity}, server-assigned {@code BIGSERIAL}, {@code status}
 * string). The producer now supplies the row's natural key ({@code event_id},
 * generated by the serializer and reused here so the row id matches the envelope
 * {@code eventId}), the domain {@code occurredAt}, and {@code aggregate_*}.
 *
 * <p>{@code partition_key} is left {@code null}: the v2 publisher falls back to
 * {@code aggregateId} as the Kafka record key, which exactly preserves the v1
 * key (per-aggregate ordering per {@code master-events.md} § Topic Layout).
 */
public class OutboxDomainEventAdapter implements DomainEventPort {

    private final MasterOutboxRepository outboxRepository;
    private final EventEnvelopeSerializer envelopeSerializer;

    public OutboxDomainEventAdapter(MasterOutboxRepository outboxRepository,
                                    EventEnvelopeSerializer envelopeSerializer) {
        this.outboxRepository = outboxRepository;
        this.envelopeSerializer = envelopeSerializer;
    }

    @Override
    public void publish(List<DomainEvent> events) {
        for (DomainEvent event : events) {
            EventEnvelopeSerializer.Serialised serialised = envelopeSerializer.serialize(event);
            MasterOutboxEntity row = new MasterOutboxEntity(
                    serialised.eventId(),
                    event.eventType(),
                    event.aggregateType(),
                    event.aggregateId().toString(),
                    null, // partition_key: publisher falls back to aggregateId
                    serialised.json(),
                    event.occurredAt());
            outboxRepository.save(row);
        }
    }
}
